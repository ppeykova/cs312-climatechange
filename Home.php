<p>Most people don't realize how much food they throw away every day — from uneaten leftovers to spoiled produce. More than 96 percent of the food we throw away ends up in landfills. In 2011, we  put more than 36 million tons of food waste in the landfill. Once in landfills, food breaks down to produce methane, a potent greenhouse gas which contributes to climate change.</p>

<p><b>Benefits of Reducing Wasted Food:</b></p>
<p>• Saves money from buying less food.</p>
<p>• Reduces methane emissions from landfills and lowers your carbon footprint.</p>
<p>• Conserves energy and resources, preventing pollution involved in the growing, manufacturing, transporting, and selling food (not to mention hauling the food waste and then putting it in the landfill).</p>
<p>• Supports your community by providing donated untouched food that would have otherwise gone to waste to those who might not have a steady food supply.</p>

<p><b>Here are a few tips to help you get started:</b></p>

<p>• Plan ahead and buy only what you need. Going to the store without a plan or on an empty stomach can lead to buying more than we need. To keep your kitchen on track, try to eat leftovers, think of meals you might eat out, and avoid unnecessary purchases by planning your grocery list ahead of time. </p>

<p>• Use your freezer. While there are plenty of benefits to eating fresh food, frozen foods can be just as nutritious. They also stay edible for much longer. A lot of seafood, for example, is frozen before it reaches your supermarket and then thawed and put on display. That means it will only stay fresh for a few days. By buying frozen seafood, you can extend the shelf life of the product considerably. Cooking and freezing food—especially produce—before it goes bad is a great way to avoid having to toss it.</p>

<p>• Be creative with leftovers. Before you shop, use the food you already have. Websites like Big Oven, Supercook, and MyFridgeFood allow you to search for recipes based on ingredients already in your kitchen. You can also use apps like Epicurious and Allrecipes to make the most of what's in your fridge and pantry.</p>

<p>• Blend, bake, or boil. Fruits and vegetables that are beyond ripe may not look pretty, but that doesn't mean they can't still taste delicious in recipes. Try using your wilting, browning, or imperfect produce to make sweet smoothies, bread, jams, sauces, or soup stocks.</p>

<p>• Talk it up. Preventing food waste is the most effective way to shrink its impact on the planet. If we avoid producing food that we don't eat, we can save the land, water, and energy that would have been used to make it. And awareness is a good first step; according to ReFED, educating consumers about food waste could prevent 2.3 million tons of greenhouse gas emissions.</p>

